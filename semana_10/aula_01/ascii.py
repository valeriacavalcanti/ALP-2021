# exibir os elementos da tabela ascii

# os símbolos numéricos
for i in range(48, 58): # 48 49 50 51 ... 57
    print(i, chr(i))

# os símbolos do alfabeto maiúsculo
for i in range(65, 91): # 65 66 67 ... 90
    print(i, chr(i))

# os símbolos do alfabeto minúsculo
for i in range(97, 123): # 97 ... 122
    print(i, chr(i))
